function CapabilitySet() {
    this.capabilitySetType = 0;
    this.capabilityData = null;
}

function parseCapabilitySet(r) {
    const set = new CapabilitySet();

    set.capabilitySetType = r.uint16(true);
    const lengthCapability = r.uint16(true);
    set.capabilityData = r.blob(lengthCapability - 4)

    return set;
}
